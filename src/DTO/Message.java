package DTO;

/**
 *
 * @author Admin
 */
public class Message {
    public String sender, message;
    
    public Message(){
        
    }

    public Message(String sender, String message) {
        this.sender = sender;
        this.message = message;
    }

    public String getSender() {
        return sender;
    }

    public void setSender(String sender) {
        this.sender = sender;
    }

    
}
